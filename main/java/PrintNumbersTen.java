public class PrintNumbersTen {
    public static void printNum10For() {
        for (int i = 0; i < 11; i++) {
            System.out.println("The current number is: " + i);
        }
    }

    public static void printNum10While() {
        int i = 0;
        while (i <= 10) {
            System.out.println("The current number is: " + i);
            i++;
        }
    }
}
